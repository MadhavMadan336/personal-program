# Dictionary containing information about a person
person = {
    "name": "John",
    "age": 30,
    "city": "New York"
}

# Tuple representing coordinates
coordinates = (10, 20)

# List of favorite colors
colors = ["blue", "green", "red"]

# Array of numbers using NumPy (you'll need to install the numpy package)
import numpy as np
numbers_array = np.array([1, 2, 3, 4, 5])

print("Person:", person)
print("Coordinates:", coordinates)
print("Favorite Colors:", colors)
print("Numbers Array:", numbers_array)
