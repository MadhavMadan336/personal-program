# Create an array of tuples
array_of_tuples = [(1, 'apple'), (2, 'banana'), (3, 'orange')]

# Accessing elements
print(array_of_tuples[0])  # Output: (1, 'apple')
print(array_of_tuples[1])  # Output: (2, 'banana')

# Accessing elements within a tuple
print(array_of_tuples[0][0])  # Output: 1
print(array_of_tuples[0][1])  # Output: 'apple'
